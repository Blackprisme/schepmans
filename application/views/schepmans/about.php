<h2>Portrait</h2>
<p>
Femme d'engagement, sincère et tenace, je m'implique à 100 % pour Bruxelles, ma Région, ma commune et leurs habitants … Bref flashback sur ce parcours long
<p> de près de 30 ans … !</p>
<br>
<p>Si je suis née à Berchem-Sainte-Agathe le 18 juin 1960, c'est bien à Molenbeek-Saint-Jean, la commune de mon cœur, que je vis depuis ma plus tendre enfance.
<p> J’y devins, de 1989 à 2001, échevine des Travaux publics, de la Culture française et de la Jeunesse. Dans le même temps, de 1989 à 1995, je serai également
<p> assistante du groupe PRL au Conseil de la Communauté française.</p>
<br>

<p>Cette double expérience - communale et d'initiation aux arcanes parlementaires - m'a permis de briguer avec succès un mandat au Parlement régional bruxellois
<p> en juin 1995. A la suite de cela, j'ai été réélue dans cette assise parlementaire durant trois législatures successives (1999, 2004 et 2009).</p>
<br>
<p>En octobre 2001, je deviens Présidente du Parlement de la Communauté française Wallonie-Bruxelles. Durant mon mandat (2001-2004), mon objectif fut de tenter
<p>de rapprocher l'institution des citoyens en y organisant, notamment, des expositions, des salons littéraires, des conférences et des débats sur des thèmes de 
<p>société.</p>
<br>
 

<p>En octobre 2006, suite au scrutin communal, le MR regagne les rangs de la majorité à Molenbeek-Saint-Jean. Durant la législature 2006-2012, j'ai exercé, comme
<p>Première échevine, trois compétences importantes telles que l’Economie, le Commerce et la Culture française.</p>
<br>
<p>Le 29 juin 2007, suite au décès inopiné de Jacques Simonet, je lui succède au poste de chef de groupe MR au Parlement régional bruxellois jusqu'au 2009.</p>
<br>
<p>Surviennent ensuite les élections communales d'octobre 2012, où le MR réalise un excellent score et qui provoque un chamboulement: une coalition inédite,
<p>réunissant le MR, le cdH et Ecolo/Groen se forme, mettant aussi fin à plusieurs décennies de mayorat socialiste. Je deviens bourgmestre de Molenbeek dans le 
<p>souci d'apporter le changement promis à nos habitants. Dans mes compétences, j'ai notamment la Police, les Affaires légales, le Personnel communal, l'Instruction
<p>publique francophone, la Petite enfance, les Crèches francophones, les Cultes et laïcité, l'Egalité des chances, la Culture française, les Bibliothèques francophones, 
<p>les Festivités, les Monuments.</p>
<br>
<p>Dans la foulée de ce nouveau défi, je quitte le Parlement bruxellois pour rejoindre, à l'issue des élections fédérales du 25 mai 2014, la Chambre des Représentants
<p>où je suis élue députée et vice-Présidente du Parlement.</p>
<br>
<p>Au travers de toutes mes responsabilités, la promotion de la citoyenneté et la participation sont l’épine dorsale de mon engagement. Plus précisément, via la Région
<p>et désormais le niveau fédéral, je me consacre à donner aux citoyens une meilleure qualité de vie (cela passe par la propreté, la sécurité, un logement salubre, la
<p>rénovation urbaine, etc.). La revitalisation économique et commerciale retient également toute mon attention. Je suis particulièrement vigilante aux questions relatives
<p>à l’émancipation des individus en leur donnant les moyens d’être vraiment maîtres de leur destin. Cela passe, entre autres, par l'apprentissage des langues et de la
<p>citoyenneté.</p>
<br>
<p>Mon parcours le démontre : pour moi, la politique, c'est une passion de plus de 30 ans et c’est toute une vie consacrée aux Bruxellois en général et aux Molenbeekois
<p>en particulier! Parce que « Bleue de Bruxelles ! » et de ses habitants, tout simplement !</p>
