<h1> Françoise Schepmans</h1>
    

<p> Françoise A.M. Schepmans (née le 18 juin 1960 à Berchem-Sainte-Agathe) est une femme politique belge, fille de Jacques Schepmans, dernier directeur général de l'hebdomadaire Pourquoi Pas ?.</p>
<p>Elle occupe actuellement les fonctions de députée fédérale, Vice-présidente de la Chambre des représentants ainsi que bourgmestre de la commune de Molenbeek-Saint-Jean.Le 3 mars 2008, elle est élue pour la première fois administratrice cooptée au conseil d'administration de l'Université libre de Bruxelles.</p>


